package com.benyissa.Examples.LearnBuilder.Implementation2;

public class Person {

    String name;
    String email;
    String city;
    int age;

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
